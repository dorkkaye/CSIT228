import java.util.*;
public class Person {
    private String name;
    private List<MobileNumber> numbers;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public List<MobileNumber> getNumbers() {
        return numbers;
    }
    public void setNumbers(List<MobileNumber> numbers) {
        this.numbers = numbers;
    }
}
