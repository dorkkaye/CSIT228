    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String name = txtTaskName.getText();
        String deadlineTask = dateFormat.format(deadline.getDate());
        String description = txtTaskDescription.getText();
        int id = Integer.parseInt(txtID.getText());

        boolean upd = dbh.insertTaskStudent(id, name, deadlineTask, description);

        if(upd)
        JOptionPane.showMessageDialog(this, "Successfuly Signed Updated");
        else
        JOptionPane.showMessageDialog(this, "Sorry, you have invalid inputs at some fields");

        try {
            java.sql.ResultSet rs = dbh.displayByTask(id);

            while(tblDisplay.getRowCount() > 0)
            {
                ((DefaultTableModel) tblDisplay.getModel()).removeRow(0);
            }

            int columns = rs.getMetaData().getColumnCount();

            while(rs.next())
            {
                Object[] row = new Object[columns];

                for(int i = 1; i <= columns; i++)
                {
                    row[i - 1] = rs.getObject(i);
                }
                ((DefaultTableModel) tblDisplay.getModel()).insertRow(rs.getRow()-1, row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(displayRecords.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

