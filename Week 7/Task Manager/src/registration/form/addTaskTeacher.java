    private void tblDisplayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDisplayMouseClicked
        try {
            DefaultTableModel model = (DefaultTableModel)tblDisplay.getModel();
            Date date = (Date) new SimpleDateFormat("yyyy-MM-dd").parse((String) model.getValueAt(tblDisplay.getSelectedRow(), 2).toString());
            String name = model.getValueAt(tblDisplay.getSelectedRow(), 1).toString();
            String description = model.getValueAt(tblDisplay.getSelectedRow(), 3).toString();
            //          int id = Integer.parseInt(model.getValueAt(tblDisplay.getSelectedRow(), 0).toString());
            deadline.setDate(date);
            txtTaskName.setText(name);
            txtTaskDescription.setText(description);
            //            txtID.setText(id);
        } catch (ParseException ex) {
            Logger.getLogger(addTaskTeacher.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblDisplayMouseClicked

