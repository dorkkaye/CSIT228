    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
            int idn = Integer.parseInt(txtIDNumber.getText());
            java.sql.ResultSet rs = dbh.displayByIDStudent(idn);

            while(rs.next())
            {
                txtPassword.setText(rs.getString(5));
                txtAge.setText(rs.getString(6));
                txtUsername.setText(rs.getString(7));
                txtEmail.setText(rs.getString(8));
            }
        } catch (SQLException ex)
        {
            Logger.getLogger(updateStudentRecord.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

