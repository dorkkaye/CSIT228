    private void btnUpdatePersonalinformation1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePersonalinformation1ActionPerformed
        int idn = Integer.parseInt(txtIDNumber.getText());
        String pass = txtPassword.getText();
        int age = Integer.parseInt(txtAge.getText());
        String user = txtUsername.getText();
        String email = txtEmail.getText();

        boolean upd = dbh.updateStudentRecord(idn, pass, age, user, email);

        if(upd)
        JOptionPane.showMessageDialog(this, "Successfuly Updated");
        else
        JOptionPane.showMessageDialog(this, "Sorry, you have invalid inputs at some fields");

        new updateTeacherRecord().setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_btnUpdatePersonalinformation1ActionPerformed

